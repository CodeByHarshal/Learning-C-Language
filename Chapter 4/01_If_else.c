#include <stdio.h>

int main()
{

    // In control statement we not store result of operation in variable we only check conditions

    // if else

    int x = 5;
    int y = 200;

    if (x > 5)
    {
        printf(" %d is greater than %d \n", x, y);
    }
    else
    {
        printf(" %d is greater than %d \n", y, x);
    }

    return 0;
}