#include <stdio.h>

int main()
{

    // Relational Operators

    char a, b;
    a = 5;
    b = 8;
    printf("%d \n", a == b);
    printf("%d \n", a != b);
    printf("%d \n", a < b);
    printf("%d \n", a > b);
    printf("%d \n", a >= b);
    printf("%d \n", a <= b);

    return 0;
}