#include <stdio.h>

int main()
{

    // Logical Operators
    /*  AND - &
        OR - |
        NOT - ~
        EXOR - ^

    All this operations are bitwise
    */

    int a, b;
    printf(" Logical AND : %d \n", a & b);
    printf(" Logical OR : %d \n", a | b);
    printf(" Logical NOT : %d \n", ~a);
    printf(" Logical EXOR : %d \n", a ^ b);

    return 0;
}